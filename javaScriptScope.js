/* JavaScript Scope

 - Decalarations are always hoisted to top of their containing scope
 - JavaScript has function-level scope

// Names enter a Scope in 4 different ways

1.) Language-defined: All scopes are, by default, given 
    the names this and arguments.
    
2.) Formal parameters: Functions can have named formal
    parameters, which are scoped to the body of that function.

3.) Function declarations: These are of the form function foo() {}.

4.) Variable declarations: These take the form var foo;

*/